public class MathClass {
    public static void main (String[] args){
        System.out.println(Math.max(5, 10)); // Printa o maior
        System.out.println(Math.min(5, 10)); // Printa o menor
        System.out.println(Math.sqrt(64)); // Printa a raiz quadrada
        System.out.println(Math.abs(-10)); // Printa o valor em positivo
        System.out.println(Math.pow(2, 3)); // Printa o valor de x na potencia y (x, y)
        System.out.println(Math.round(4.6)); // Arredonda o valor para o int mais proximo
        System.out.println(Math.ceil(4.1)); // Arrendonda o valor para cima
        System.out.println(Math.floor(4.9)); // Arredonda o valor para baixo
    }
}
