public class Variaveis {
    public static void main(String[] args){
        String nome = "Pedro";
        int a = 5;
        float b = 5.5f;
        char c = 'C';
        boolean booleano = true;
        
        System.out.println(nome);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(booleano);
    }
}
